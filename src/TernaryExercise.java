public class TernaryExercise {
    public static void main(String[] args) {
        int age = 27;
        int x = 7;
        String word = "Java";

        // Age
        System.out.println(age >= 18 ? "Adult" : "Minor");
        // Positives
        System.out.println(x > 0 ? "Positive" : "Not Positive");
        //Word.Equals
        System.out.println(word.equals("Java") ? "Cool" : "Meh");

    }
}
